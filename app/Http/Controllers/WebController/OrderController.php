    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $records=Order::where(function ($query) use($request){
            if ($request->has('search')){
                $query->whereHas('restaurant', function ($restaurant) use ($request){
                    $restaurant->where('name','like','%'.$request->search.'%');
                });
                $query->orWhere(function ($query) use ($request){
                    $query->where('status','like','%'.$request->search.'%')
                        ->orwhere('notes','like','%'.$request->search.'%');
                });
            }

        })->with('restaurant')->latest()->paginate();
        return view('admin.orders.index',compact('records'));

    }

